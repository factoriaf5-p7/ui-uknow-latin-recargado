import React, { useState } from "react";
import { Form, Button, Alert } from "react-bootstrap";
import { CreateContent, ContentData } from "../../services/upload.service";
import "./UpContent.css";
import ContentEdit from "./ContentEdit";
import { useNavigate } from "react-router-dom";
import rocketEmoji from "../../assets/rocketEmoji.png"; 

const ContentForm = () => {
  //obtiene el nombre del usuario
  const [title, setTitle] = useState("");
  const [description, setDescription] = useState("");
  const [price, setPrice] = useState(0);
  const [category, setCategory] = useState("");
  const [dificulty, setDificulty] = useState(0);
  const [content, setContent] = useState("");
  const [showSuccessAlert, setShowSuccessAlert] = useState(false);
  const [showErrorAlert, setShowErrorAlert] = useState(false);
  const [isLoading, setIsLoading] = useState(false);  
  const navigate = useNavigate();

  const handleSubmit = (event: React.FormEvent<HTMLFormElement>) => {
    event.preventDefault();
    const formData: ContentData = {
      title,
      description,
      price,
      category,
      dificulty,
      content,
    };

    const user: string | null = localStorage.getItem("user_id");
    setIsLoading(true); // Activa el cÃ­rculo de carga
    CreateContent(user, formData) //llama a la funciÃ³n de upload.service.ts
      .then((response) => {
        console.log("Solicitud exitosa:", response.data);
        setShowSuccessAlert(true);
        setShowErrorAlert(false);
        //limpia los campos
        setTitle("");
        setDescription("");
        setPrice(0);
        setCategory("");
        setDificulty(0);
        setContent("");
        setTimeout(() => {
        setIsLoading(false);
        navigate("/mycontent");
      }, 1000);
      })
        .catch((error) => {
        console.error("Error en la solicitud:", error);
        setShowSuccessAlert(false);
        setShowErrorAlert(true);
        setIsLoading(false); // En caso de error, desactiva el cÃ­rculo
      });
  };

  return (
    <div className="container mt-5">
      {showSuccessAlert && (
        <Alert variant="success" onClose={() => setShowSuccessAlert(false)} dismissible>
          Â¡El contenido se cargÃ³ correctamente!
        </Alert>
      )}

      {showErrorAlert && (
        <Alert variant="danger" onClose={() => setShowErrorAlert(false)} dismissible>
          Â¡Error al cargar el contenido! Por favor, intÃ©ntalo nuevamente mÃ¡s tarde.
        </Alert>
      )}

{isLoading ? (
        // Si isLoading es verdadero, muestra el emoticon ðŸš€ centrado
        <img src={rocketEmoji} alt="Emoticon Rocket" className="rocket-emoji" />
      ) : (
        // Si isLoading es falso, muestra el formulario normal


      <Form onSubmit={handleSubmit}>
        <Form.Group className="mb-3" controlId="formTitle">
          <Form.Label>TÃ­tulo</Form.Label>
          <Form.Control
            type="text"
            placeholder="Ingrese el tÃ­tulo"
            value={title}
            onChange={(e) => setTitle(e.target.value)}
            required
          />
        </Form.Group>

        <Form.Group className="mb-3" controlId="formDescription">
          <Form.Label>DescripciÃ³n del Contenido</Form.Label>
          <Form.Control
            as="textarea"
            rows={3}
            placeholder="Ingrese la descripciÃ³n del contenido"
            value={description}
            onChange={(e) => setDescription(e.target.value)}
            required
          />
        </Form.Group>

        <Form.Group className="mb-3" controlId="formPrice">
          <Form.Label>Precio</Form.Label>
          <Form.Control
            type="number"
            step="0.01"
            placeholder="Ingrese el precio"
            value={price}
            onChange={(e) => setPrice(Number(e.target.value))}
            required
          />
        </Form.Group>

        <Form.Group className="mb-3" controlId="formCategory">
          <Form.Label>CategorÃ­a</Form.Label>
          <Form.Control
            type="text"
            placeholder="Ingrese la categorÃ­a"
            value={category}
            onChange={(e) => setCategory(e.target.value)}
            required
          />
        </Form.Group>

        <Form.Group className="mb-3" controlId="formDificulty">
          <Form.Label>Dificultad</Form.Label>
          <Form.Control
            type="number"
            min="1"
            max="5"
            placeholder="Ingrese la dificultad"
            value={dificulty}
            onChange={(e) => setDificulty(Number(e.target.value))}
            required
          />
        </Form.Group>

        <Form.Group className="mb-3" controlId="formContent">
  <Form.Label>Contenido Completo</Form.Label>
  <ContentEdit
    value={content}
    onChange={(val) => setContent(val)}
    required
  />
</Form.Group>


        <Button variant="primary" type="submit">
          Enviar
        </Button>
        </Form>
      )}
    </div>
  );
};

export default ContentForm;